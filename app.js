        varnishYellowing: c.varnishYellowing !== undefined ? c.varnishYellowing : 0.0,
        surfaceGrime: c.surfaceGrime !== undefined ? c.surfaceGrime : 0.0,
        paintChalking: c.paintChalking !== undefined ? c.paintChalking : 0.0,
